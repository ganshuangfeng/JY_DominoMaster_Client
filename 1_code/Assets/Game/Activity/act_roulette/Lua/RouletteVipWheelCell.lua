local basefunc = require "Game/Common/basefunc"

RouletteVipWheelCell = basefunc.class()
local C = RouletteVipWheelCell
C.name = "RouletteVipWheelCell"

function C.Create(parent, data, r)
	return C.New(parent, data, r)
end

function C:AddMsgListener()
    for proto_name,func in pairs(self.lister) do
        Event.AddListener(proto_name, func)
    end
end

function C:MakeLister()
    self.lister = {}
    self.lister["game_language_change_msg"] = basefunc.handler(self, self.RefreshLL)
end

function C:RemoveListener()
    for proto_name,func in pairs(self.lister) do
        Event.RemoveListener(proto_name, func)
    end
    self.lister = {}
end

function C:MyExit()
	self:RemoveListener()
	destroy(self.gameObject)
end

function C:OnDestroy()
	self:MyExit()
end

function C:MyClose()
	self:MyExit()
end

function C:ctor(parent, data, r)
	self.parent = parent
	self.data = data
	self.r = r
	ExtPanel.ExtMsg(self)
	
	local obj = newObject(C.name, parent)
	local tran = obj.transform
	self.transform = tran
	self.gameObject = obj
	LuaHelper.GeneratingVar(self.transform, self)
	
	self:MakeLister()
	self:AddMsgListener()
	self:InitUI()
	self:InitLL()
end

function C:InitLL()
end

function C:RefreshLL()
end

function C:InitUI()
	self:MyRefresh()
end

function C:MyRefresh()
	self.award_txt.text = StringHelper.ToCash(self.data.award)
	self.award_img.sprite = GetTexture(self.data.icon)
	local s = self.data.scale or 1
	self.award_img.transform.localScale = Vector3.New(s, s, 1)
	self.transform.localRotation = Quaternion:SetEuler(0, 0, self.r)
end
